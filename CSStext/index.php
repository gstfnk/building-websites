    <div class="column">
        Nyan Cat is the name of a YouTube video uploaded in April 2011, which became an internet meme.
        The video merged a Japanese pop song with an animated cartoon cat with a Pop-Tart for a torso, flying
        through space, and leaving a rainbow trail behind it. The video ranked at number 5 on the list of most
        viewed YouTube videos in 2011. On April 2, 2011, the GIF animation of the cat was posted by 25-year-old
        Christopher Torres of Dallas, Texas, who uses the name "prguitarman", on his website LOL-Comics. Torres
        explained in an interview where the idea for the animation came from: "I was doing a donation drive for the
        Red Cross and in-between drawings in my Livestream video chat, two different people mentioned I should draw
        a 'Pop Tart' and a 'cat'." In response, he created a hybrid image of a Pop-Tart and a cat, which was
        developed a few days later into the animated GIF. The design of Nyan Cat was influenced by Torres' pet cat
        Marty, who died in November 2012 from feline infectious peritonitis. The original version of the song
        "Nyanyanyanyanyanyanya!" was uploaded by user "daniwell" to the Japanese video site Niconico on July 25,
        2010. The song features the Vocaloid virtual singer Hatsune Miku. The Japanese word nyan is onomatopoeic,
        imitating the call of a cat (equivalent to English "meow"). The song was later included in the rhythm game
        Hatsune Miku: Project DIVA F, released by Sega in August 2012. On January 30, 2011, a user named "Momomomo"
        uploaded a cover of "Nyanyanyanyanyanyanya!" featuring the UTAU voice Momone Momo. The voice source used to
        create the Momone Momo voice was Momoko Fujimoto, a Japanese woman who lives in Tokyo.
